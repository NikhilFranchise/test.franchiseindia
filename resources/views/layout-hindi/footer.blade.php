            $.ajax({
                url: '/dealers-search/' + search_keyword,
                type: 'GET',
                dataType: 'json',
                success: function(response) {
                    prepareList(JSON.parse(JSON.stringify(response)));

                },
                error: function(err) {
                    console.log(err);

                }
            });
        }

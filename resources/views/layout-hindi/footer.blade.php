        if ($("#dealer-bar-search-top").val() != "") {
            var value = $("#dealer-bar-search-top").val();
            var items = value.split(' - <strong> in');
            if (items.length > 1)
                value = items[0];
            window.location.href = '/dealers-india/search/' + value;
